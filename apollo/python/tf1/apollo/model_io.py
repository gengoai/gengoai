import keras as K
import tensorflow as tf


def save_model(model, location):
    with K.backend.get_session() as sess:
        tf.saved_model.simple_save(
            sess,
            location,
            inputs={t.name: t for t in model.inputs},
            outputs={t.name: t for t in model.outputs})
